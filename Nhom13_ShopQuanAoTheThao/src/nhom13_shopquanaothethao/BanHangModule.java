    private void SLTon(String ten, String size)
    {
        try
        {
            con = nhom13_shopquanaothethao.Connection.GetConnection();
            String sql = "SELECT SoLuongTon, TinhTrang FROM SANPHAM WHERE TENSANPHAM = N'" + ten + "' AND SIZE = '" + size +"'";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs.next())
            {
                txt_SLTon.setText(String.valueOf(rs.getInt("SoLuongTon")));
                lbl_TinhTrang.setVisible(true);
                if (Integer.parseInt(txt_SLTon.getText()) != 0)
                {
                    lbl_TinhTrang.setText("Còn Hàng");
                    lbl_TinhTrang.setForeground(Color.GREEN);
                }
                else if (rs.getString("TinhTrang").equals("Chưa Nhập Sản Phẩm"))
                {
                    lbl_TinhTrang.setText("Chưa nhập sản phẩm này");
                    lbl_TinhTrang.setForeground(Color.red);
                }
                else
                {
                    lbl_TinhTrang.setText("Hết Hàng");
                    lbl_TinhTrang.setForeground(Color.red);
                }
            }
            else
            {
                txt_SLTon.setText(0+"");
                lbl_TinhTrang.setVisible(true);
                lbl_TinhTrang.setText("Chưa có sản phẩm này");
                lbl_TinhTrang.setForeground(Color.red);
            }
            st.close();
            rs.close();
            con.close();
        }
        catch (SQLException e)
        {
            e.printStackTrace();
        }
    }
