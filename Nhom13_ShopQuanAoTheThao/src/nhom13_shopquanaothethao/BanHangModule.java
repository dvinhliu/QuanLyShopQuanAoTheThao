    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        // TODO add your handling code here:
        if (lbl_TinhTrang.getText().equals("Chưa có sản phẩm này"))
        {
            JOptionPane.showMessageDialog(rootPane, "Chưa có sản phẩm này", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else
        {
            if (txt_SL.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng kiểm tra lại thông tin", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            else if (Integer.parseInt(txt_SL.getText().trim()) > Integer.parseInt(txt_SLTon.getText().trim()))
            {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng kiểm tra lại số lượng <= số lượng tồn kho", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            else if (Integer.parseInt(txt_SL.getText().trim()) == 0)
            {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng không nhập số lượng = 0", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            else if (Integer.parseInt(txt_SLTon.getText().trim()) == 0)
            {
                JOptionPane.showMessageDialog(rootPane, "Hết hàng vui lòng chọn size khác", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        }
        for (int i = 0; i < BHF.tblmodel.getRowCount(); i++)
        {
            if (BHF.tblmodel.getValueAt(i, 1).toString().equals(pro.getTensp()) && BHF.tblmodel.getValueAt(i, 3).toString().equals(sSize))
            {
                BHF.tblmodel.setValueAt(txt_SL.getText(), i, 2);
                BHF.tblmodel.setValueAt(String.valueOf(Integer.parseInt(BHF.tblmodel.getValueAt(i, 2).toString()) * Integer.parseInt(BHF.tblmodel.getValueAt(i, 4).toString())), i, 5);
                BHF.GetTotal();
                txt_SL.setText("");
                txt_SL.requestFocus();
                return;
            }
        }
        Object[] row = { 0, pro.getTensp(), txt_SL.getText(),  sSize, String.valueOf(pro.getGiaban()), String.valueOf(Integer.parseInt(txt_SL.getText()) * pro.getGiaban()) };
        BHF.tblmodel.addRow(row);
        BHF.loadSTT();
        BHF.GetTotal();
        txt_SL.setText("");
        txt_SL.requestFocus();
    }//GEN-LAST:event_btn_SaveActionPerformed

