    private String getDayOfWeekInVietnamese(DayOfWeek dayOfWeek) 
    {
        switch (dayOfWeek) 
        {
            case SUNDAY:
                return "Chủ nhật";
            case MONDAY:
                return "Thứ hai";
            case TUESDAY:
                return "Thứ ba";
            case WEDNESDAY:
                return "Thứ tư";
            case THURSDAY:
                return "Thứ năm";
            case FRIDAY:
                return "Thứ sáu";
            case SATURDAY:
                return "Thứ bảy";
            default:
                return "";
        }
    }
