    private void openMenubar()
    {
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i < width; i++)
                {
                    pnMenu.setSize(i, height);
                    try 
                    {
                        Thread.sleep(2);
                    } 
                    catch (InterruptedException ex) {
                        Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }).start();
    }
    private void closeMenubar()
    {
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = width; i > 0; i--)
                {
                    pnMenu.setSize(i, height);
                    try 
                    {
                        Thread.sleep(2);
                    } 
                    catch (InterruptedException ex) {
                        Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }).start();
    }
    
    private JFrame activeJFrame = null;
    private void openChildJFrame(JFrame childJFrame)
    {
        if (activeJFrame != null)
        {
            activeJFrame.dispose();
        }
        else
        {
            activeJFrame = childJFrame;
            childJFrame.setBounds(0, 0, pnChilds.getWidth(), pnChilds.getHeight());
            pnChilds.removeAll();
            pnChilds.add(activeJFrame.getContentPane());
            pnChilds.revalidate();
            pnChilds.repaint();
        }
    }
