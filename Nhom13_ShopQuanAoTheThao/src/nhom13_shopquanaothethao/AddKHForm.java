    private boolean KT_SDT(String sdt)
    {
        try
        {
           con = nhom13_shopquanaothethao.Connection.GetConnection();
           String sql = "SELECT * FROM KHACHHANG WHERE SDT = '" + sdt + "'";
           Statement st = con.createStatement();
           ResultSet rs = st.executeQuery(sql);
           if (rs.next())
           {
               st.close();
               rs.close();
               con.close();
               return false;
           }
           else
           {
                st.close();
                rs.close();
                con.close();
                return true;
           }
        }
        catch (SQLException e)
        {
            e.printStackTrace();
        }
        return false;
    }
