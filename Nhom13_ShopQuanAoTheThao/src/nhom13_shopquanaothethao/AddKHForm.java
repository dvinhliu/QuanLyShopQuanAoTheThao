    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
        if (!txt_TenKH.getText().isEmpty() && !txt_SDT.getText().isEmpty())
        {
            if (txt_SDT.getText().length() != 10 || !txt_SDT.getText().startsWith("0"))
            {
                JOptionPane.showMessageDialog(rootPane, "Yêu cầu SĐT phải 10 chữ số và bắt đầu bằng số 0", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            else
            {
                try
                {
                    if (JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn thêm khách hàng này không", "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION)
                    {
                        if (KT_SDT(txt_SDT.getText()))
                        {
                            con = nhom13_shopquanaothethao.Connection.GetConnection();
                            String sql = "INSERT INTO KHACHHANG VALUES (?, ?, ?)";
                            PreparedStatement ps = con.prepareStatement(sql);
                            ps.setString(1, txt_IDKH.getText());
                            ps.setString(2, txt_TenKH.getText());
                            ps.setString(3, txt_SDT.getText());
                            int x = ps.executeUpdate();
                            if (x >= 1)
                            {
                                JOptionPane.showMessageDialog(rootPane, "Thêm Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                                btn_HuyActionPerformed(null);
                                ps.close();
                                con.close();
                                this.dispose();
                            }
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(rootPane, "Trùng SĐT", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        }
                    }
                }
                catch (SQLException e)
                {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btn_ThemActionPerformed

